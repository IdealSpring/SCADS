    /**
     * Returns a list-iterator of the elements in this list (in proper
     * sequence), starting at the specified position in the list.
     * Obeys the general contract of {@code List.listIterator(int)}.<p>
     *
     * The list-iterator is <i>fail-fast</i>: if the list is structurally
     * modified at any time after the Iterator is created, in any way except
     * through the list-iterator's own {@code remove} or {@code add}
     * methods, the list-iterator will throw a
     * {@code ConcurrentModificationException}.  Thus, in the face of
     * concurrent modification, the iterator fails quickly and cleanly, rather
     * than risking arbitrary, non-deterministic behavior at an undetermined
     * time in the future.
     *
     * @param index index of the first element to be returned from the
     *              list-iterator (by a call to {@code next})
     * @return a ListIterator of the elements in this list (in proper
     *         sequence), starting at the specified position in the list
     * @throws IndexOutOfBoundsException {@inheritDoc}
     * @see List#listIterator(int)
     */
    /**
     * 功能：获取从指定位置开始的链表迭代器
     */
    public ListIterator<E> listIterator(int index) {
        checkPositionIndex(index);
        return new ListItr(index);
    }

    /**
     * 迭代器实现类
     */
    private class ListItr implements ListIterator<E> {
        private Node<E> lastReturned;       // 保存移动指针索引节点后的前一个节点
        private Node<E> next;       // 当前索引对应的节点
        private int nextIndex;      // 索引
        private int expectedModCount = modCount;        // 预期修改次数

        ListItr(int index) {        // 初始化指定索引的元素和元素索引指针
            // assert isPositionIndex(index);
            next = (index == size) ? null : node(index);
            nextIndex = index;
        }

        public boolean hasNext() {      // 判断时候还有下一个元素
            return nextIndex < size;
        }

        public E next() {       // 获取下一个元素
            checkForComodification();
            if (!hasNext())
                throw new NoSuchElementException();

            lastReturned = next;
            next = next.next;
            nextIndex++;
            return lastReturned.item;
        }

        public boolean hasPrevious() {      // 时候还有前一个元素
            return nextIndex > 0;
        }

        public E previous() {       // 获取前一个元素
            checkForComodification();
            if (!hasPrevious())
                throw new NoSuchElementException();

            lastReturned = next = (next == null) ? last : next.prev;
            nextIndex--;
            return lastReturned.item;
        }

        public int nextIndex() {        // 获取下一个节点索引
            return nextIndex;
        }

        public int previousIndex() {        // 获取上一个节点索引
            return nextIndex - 1;
        }

        public void remove() {      //移除节点
            checkForComodification();
            if (lastReturned == null)
                throw new IllegalStateException();

            Node<E> lastNext = lastReturned.next;
            unlink(lastReturned);
            if (next == lastReturned)
                next = lastNext;
            else
                nextIndex--;
            lastReturned = null;
            expectedModCount++;
        }

        public void set(E e) {      // 替换当前节点的值域
            if (lastReturned == null)
                throw new IllegalStateException();
            checkForComodification();
            lastReturned.item = e;
        }

        public void add(E e) {      // 添加元素
            checkForComodification();
            lastReturned = null;
            if (next == null)
                linkLast(e);
            else
                linkBefore(e, next);
            nextIndex++;
            expectedModCount++;
        }

        public void forEachRemaining(Consumer<? super E> action) {
            Objects.requireNonNull(action);
            while (modCount == expectedModCount && nextIndex < size) {
                action.accept(next.item);
                lastReturned = next;
                next = next.next;
                nextIndex++;
            }
            checkForComodification();
        }

        final void checkForComodification() {
            if (modCount != expectedModCount)
                throw new ConcurrentModificationException();
        }
    }

    /**
     * 链表节点类
     */
    private static class Node<E> {
        E item;     // 值域
        Node<E> next;       // 后继
        Node<E> prev;       // 前驱

        Node(Node<E> prev, E element, Node<E> next) { // 构造方法
            this.item = element;
            this.next = next;
            this.prev = prev;
        }
    }

